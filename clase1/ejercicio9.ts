//Declara dos variables booleanas y realiza una comparación lógica (como AND, OR). Imprime el resultado. 

//Declaración de variables booleanas
let esVerdadero: boolean = true;
let esFalso: boolean = false;   

//Realización de una comparación lógica (AND)
let resultadoAnd: boolean = esVerdadero && esFalso;
console.log("Resultado de AND (true && false):", resultadoAnd); // Imprime false
//Realización de una comparación lógica (OR)
let resultadoOr: boolean = esVerdadero || esFalso;
console.log("Resultado de OR (true || false):", resultadoOr); // Imprime true

